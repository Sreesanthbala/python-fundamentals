# 1️⃣ Definition

# A dictionary is a mutable, unordered collection that stores data in key–value pairs, where each key is unique.

# 2️⃣ Why it is used

# Dictionaries allow fast data lookup, organize data meaningfully, map relationships (like name → marks), and are widely used in real-world applications.


# 3️⃣ Syntax
# dictionary_name = {
#     key1: value1,
#     key2: value2
# }

# Example

student = {"name": "Ravi", "age": 22, "course": "Python"}


# 5️⃣ Built-in methods / functions

# keys() – returns all keys

# values() – returns all values

# items() – returns key-value pairs

# get() – safe value access

# update() – add/update items

# pop() – remove item

# clear() – remove all