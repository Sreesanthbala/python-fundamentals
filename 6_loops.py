#  Definition

# A loop is a control structure that allows you to execute a block of code repeatedly until a condition is met or for a fixed number of times.


# 2. Why it is used

# Loops reduce code repetition, automate repetitive tasks, process collections (lists, files, data), and make programs efficient and readable.



# 3. Built-in methods / functions used with loops

# range() – generate numbers

# len() – length of sequence

# enumerate() – index + value

# zip() – loop over multiple sequences

# any(), all() – condition checks

# Real-world use cases
# Reading files line by line

# Processing user inputs

# Iterating over database records

# Training machine learning models

# Automating repetitive tasks (emails, reports)


